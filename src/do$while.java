public class do$while {
    public static void main(String[] args) {
        int i = 0;
        do{
            System.out.println(i);
            i=i+2;
        }while (i<10);
        System.out.println();
        do {
            System.out.println("Do...while is cool;");
        }while (2==3);
    }
}
